/// Get the max value string for a uintN type.
inline std::string maxUintValue(unsigned _bits)
{
	if (_bits == 0 || _bits > 256 || _bits % 8 != 0) return "0";
	return maxUintValues[_bits / 8 - 1];
}

/// Get the max value string for an intN type.
inline std::string maxIntValue(unsigned _bits)
{
	if (_bits == 0 || _bits > 256 || _bits % 8 != 0) return "0";
	return maxIntValues[_bits / 8 - 1];
}

/// Get the min value string for an intN type (as unsigned two's complement).
inline std::string minIntValue(unsigned _bits)
{
	if (_bits == 0 || _bits > 256 || _bits % 8 != 0) return "0";
	return minIntValues[_bits / 8 - 1];
}

